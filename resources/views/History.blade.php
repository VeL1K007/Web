        <h1>История VR индустрии</h1>
        <p class="lead">Первый прототип VR-очков появился ещё в 1837 году. Называлось это устройство — стереоскоп. Правда стереоскоп работал исключительно со статическими изображениями, но внешне он очень похож на современные очки виртуальной реальности. Работа стереоскопа проста, но для тех времён удивительна: в аппарат устанавливались два, казалось бы, одинаковых изображения, но сделанных под разным углом, таким образом создавался эффект объёмного изображения.
        </p>
        <img src="https://leonardo.osnova.io/8b028eb4-4f4f-ad2a-2ae6-6afca87f9816/" class="img-fluid border rounded-3 shadow-lg mb-4" alt="Example image" width="700" height="500" loading="lazy">
        <p class="lead">Советские стереоскопы
            Это были лишь первые и не менее важные шаги на пути к современным VR шлемам. Стереоскопов людям было недостаточно. Всё развивалось, и для погружения в иную реальность на помощь картинам пришли фотографии, а после появились кинофильмы.

            История популярных ныне 4D-кинотеатров началась с 1956 года! Тогда Мортон Хейлиг придумал как можно максимально погрузить зрителя в происходящее на экране. Придуманный им аппарат назывался Sensorama, из новшеств было подвижное сиденье с вибрацией, имитация ветра и запахов, поддержка цветной картинки со стерео звуком.
        </p>
        <img src="https://leonardo.osnova.io/4e952dce-2871-573e-a953-80423a9ee472/-/preview/400/-/format/webp/" class="img-fluid border rounded-3 shadow-lg mb-4" alt="Example image" width="500" height="350" loading="lazy">
        <p class="lead">Вполне ожидаемо, что столь интересный и в то же время дорогой проект в полном объеме реализоваться не смог. И все же, данное изобретение не только дало Мортону Хейлигу звание «отца виртуальной реальности», но и сделало большой вклад в VR индустрию. Скотт Фишер, будучи руководителем проекта виртуальной среды VIEW от NASA, говорил о большом вкладе Sensorama в его проект. К разработкам NASA вернёмся чуть позже.

            Поэтому перенесемся в 60-е годы в лабораторию компании Philco. В 1961 году Philco Company представила проект первого HMT шлема — Headsight. HMT (head motion tracking) — система, которая регистрировала повороты устройства в пространстве в соответствии с тремя углами Эйлера. Это значит, что с помощью данной системы у «зрителя» появлялась возможно осмотреться по сторонам, вращая головой. К сожалению, шлем не покинул пределы лаборатории Philco.</p>
        <img src="https://leonardo.osnova.io/106e44d9-d519-b2fe-4be7-9c81c6174817/-/preview/600/-/format/webp/" class="img-fluid border rounded-3 shadow-lg mb-4" alt="Example image" width="500" height="350" loading="lazy">
        <p class="lead">Более удачным проектом оказался The Sword of Damocle’s. С 1965 по 1968 в Гарвардсокм университете шла разработка Айвена Сазерленда и Боба Спроула. Шлем также был оснащен системой HMT. Устройство было больших габаритов и не позволяло свободно передвигаться. Данный проект считается первым шлемом дополненной реальности.
            К слову, Айвен сделал довольно большой вклад в развитие трехмерной графики и графики в целом.</p>
        <img src="https://leonardo.osnova.io/8cb1fd9d-d2a5-dd41-5d0a-bdc360b859a1/-/preview/700/-/format/webp/" class="img-fluid border rounded-3 shadow-lg mb-4" alt="Example image" width="500" height="350" loading="lazy">
        <p class="lead">Первые коммерческие устройства виртуальной реальности появились только в начале 90-ых. Компания VPL Research в 1989 представила рынку свою новую систему — EyePhone. Устройство состояло из очков виртуальной реальности и специальных перчаток (DataGloves). Цена этой «игрушки» была немаленькая — $250,000, в комплекте шли компьютеры для работы системы. Цена была не единственной проблемой, в то время «EyePhone» мог воспроизводить только 7 кадров в секунду, в отличие от телевизоров с их тридцатью кадрами.

            По вышеуказанным причинам система не получила широкого распространения, зато какой вклад она внесла в дальнейшее развитие виртуальной индустрии! Компанией VPL Research были разработаны очки, перчатки, а также целый костюм для виртуальной реальности. Для разработчиков был создан графический движок Isaac и новый язык визуального программирования Body Electric.

            Интересно, что NASA работала совместно с VPL Research по вышеупомянутому проекту VIEW. Калифорнийский отдел NASA работал над устройством шлема, а VPL в первую очередь над программной составляющей.
        </p>
        <img src="https://leonardo.osnova.io/e10f6390-5570-864f-298a-bb7c058abd1d/-/resize/391/-/format/webp/" class="img-fluid border rounded-3 shadow-lg mb-4" alt="Example image" width="350" height="250" loading="lazy">
        <p class="lead">Проект, основанный небезызвестной компанией SEGA в 1993 году, оказался в итоге так и нереализованным. Шлем должен был стать дополнением к Sega Mega Drive. Он представлял из себя уже известную HMT систему, LCD экран и стерео наушники. Первый прототип был показан в 1993 году на Summer CES специально для журналистов. Цена гаджета была всего 200$, по сравнению с предшественниками — это значительный прогресс: вес очков был небольшой, голова чувствовала себя комфортно и не болела после длительного ношения. Тогда что же привело к закрытию этот перспективный проект?

            Британская газета The Independent on Sunday после Summer CES напишет статью, где заявит о небезопасном ношении очков, после чего Стэнфордский научно-исследовательский институт заявил о возможном вреде для глаз. После этих заявлений релизы по всему миру были отменены. В октябре 1994 представитель SEGA заявил о продолжении работы над SEGA VR. Но после о проекте новостей не поступало, он был тихо закрыт, так и не дойдя до рынка потребителей.
        </p>
